package com.fina.mvc;

public class Lagu {
	public String judulLagu;
	public String genre;
	public int tahun;
	public String pencipta;
	
	public String getJudulLagu() {
		return judulLagu;
	}
	public void setJudulLagu(String judulLagu) {
		this.judulLagu = judulLagu;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public int getTahun() {
		return tahun;
	}
	public void setTahun(int tahun) {
		this.tahun = tahun;
	}
	public String getPencipta() {
		return pencipta;
	}
	public void setPencipta(String pencipta) {
		this.pencipta = pencipta;
	}
	
}
